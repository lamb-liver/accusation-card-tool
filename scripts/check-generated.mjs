#!/usr/bin/env node
import { readFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

import { INDEX_PATH, readJson } from './lib/public-assets.mjs';

const __dirname = dirname(fileURLToPath(import.meta.url));
const projectRoot = resolve(__dirname, '..');

const failures = [];

function readText(path) {
  return readFileSync(resolve(projectRoot, path), 'utf8');
}

function assertSame(path, expected) {
  const actual = readText(path);
  if (actual !== expected) {
    failures.push(`${path} is stale. Regenerate it before committing.`);
  }
}

function generateApiConstants() {
  const config = readJson(projectRoot, 'config/csrf.json', failures);
  if (!config) return null;
  const { headerName, headerValue } = config;
  if (typeof headerName !== 'string' || !headerName.trim()) {
    failures.push('config/csrf.json headerName must be a non-empty string');
  }
  if (typeof headerValue !== 'string' || !headerValue.trim()) {
    failures.push('config/csrf.json headerValue must be a non-empty string');
  }
  if (failures.length > 0) return null;
  return `// Generated by scripts/generate-api-constants.mjs — do not edit.
export const CSRF_HEADER_NAME = ${JSON.stringify(headerName)};
export const CSRF_HEADER_VALUE = ${JSON.stringify(headerValue)};
`;
}

function generateCardManifests() {
  const index = readJson(projectRoot, INDEX_PATH, failures);
  if (!index) return null;

  const cardIds = [];
  const factions = new Set();
  const cardsByIdEntries = [];

  for (const shard of index.shards ?? []) {
    const shardPath = resolve(projectRoot, 'public', shard.path.replace(/^\//, ''));
    const cards = JSON.parse(readFileSync(shardPath, 'utf8'));
    if (!Array.isArray(cards)) {
      failures.push(`shard ${shard.path} is not an array`);
      continue;
    }
    if (cards.length !== shard.count) {
      failures.push(`shard ${shard.path} count mismatch (index=${shard.count}, actual=${cards.length})`);
    }
    for (const card of cards) {
      if (!card?.id || typeof card.id !== 'string') {
        failures.push(`shard ${shard.path} has card without string id`);
        continue;
      }
      cardIds.push(card.id);
      if (card.faction) factions.add(card.faction);
      cardsByIdEntries.push([
        card.id,
        {
          faction: card.faction ?? '',
          type: card.type ?? '',
          name: card.name ?? card.id,
        },
      ]);
    }
  }

  if (cardIds.length !== index.total) {
    failures.push(`card total mismatch (index=${index.total}, actual=${cardIds.length})`);
  }

  const uniqueIds = new Set(cardIds);
  if (uniqueIds.size !== cardIds.length) failures.push('duplicate card ids across shards');

  const sortedIds = [...uniqueIds].sort();
  const sortedFactions = [...factions].sort();
  const sortedCardsById = cardsByIdEntries
    .filter(([id]) => uniqueIds.has(id))
    .sort(([a], [b]) => a.localeCompare(b));

  const backend = `// Generated by scripts/generate-card-manifest.mjs — do not edit.
export const cardIds = new Set(${JSON.stringify(sortedIds)});
export const factions = new Set(${JSON.stringify(sortedFactions)});
export const cardsById = new Map(${JSON.stringify(sortedCardsById)});
`;

  const cardNames = Object.fromEntries(sortedCardsById.map(([id, meta]) => [id, meta.name]));
  const frontend = `// Generated by scripts/generate-card-manifest.mjs — do not edit.\nexport const cardNamesById = ${JSON.stringify(cardNames, null, 2)};\n`;

  return { backend, frontend };
}

const apiConstants = generateApiConstants();
if (apiConstants) {
  assertSame('functions/_shared/csrf.generated.js', apiConstants);
  assertSame('src/api/csrfHeader.generated.js', apiConstants);
}

const cardManifests = generateCardManifests();
if (cardManifests) {
  assertSame('functions/_shared/cardManifest.generated.js', cardManifests.backend);
  assertSame('src/data/cardNames.generated.js', cardManifests.frontend);
}

if (failures.length > 0) {
  console.error('check-generated: failed');
  for (const failure of failures) console.error(`  - ${failure}`);
  process.exit(1);
}

console.log('check-generated: ok');
