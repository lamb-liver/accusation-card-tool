#!/usr/bin/env node
import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const projectRoot = resolve(__dirname, '..');
const sourcePath = resolve(projectRoot, 'config/csrf.json');

const config = JSON.parse(readFileSync(sourcePath, 'utf8'));
const headerName = config.headerName;
const headerValue = config.headerValue;

if (typeof headerName !== 'string' || !headerName.trim()) {
  console.error('FAIL: config/csrf.json headerName must be a non-empty string');
  process.exit(1);
}
if (typeof headerValue !== 'string' || !headerValue.trim()) {
  console.error('FAIL: config/csrf.json headerValue must be a non-empty string');
  process.exit(1);
}

const backendPath = resolve(projectRoot, 'functions/_shared/csrf.generated.js');
const frontendPath = resolve(projectRoot, 'src/api/csrfHeader.generated.js');

const backendContent = `// Generated by scripts/generate-api-constants.mjs — do not edit.
export const CSRF_HEADER_NAME = ${JSON.stringify(headerName)};
export const CSRF_HEADER_VALUE = ${JSON.stringify(headerValue)};
`;

const frontendContent = `// Generated by scripts/generate-api-constants.mjs — do not edit.
export const CSRF_HEADER_NAME = ${JSON.stringify(headerName)};
export const CSRF_HEADER_VALUE = ${JSON.stringify(headerValue)};
`;

writeFileSync(backendPath, backendContent, 'utf8');
mkdirSync(dirname(frontendPath), { recursive: true });
writeFileSync(frontendPath, frontendContent, 'utf8');

console.log(`OK: API constants generated (CSRF ${headerName}: ${headerValue})`);
