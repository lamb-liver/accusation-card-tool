#!/usr/bin/env node
import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const projectRoot = resolve(__dirname, '..');
const indexPath = resolve(projectRoot, 'public/cards/index.json');
const outputPath = resolve(projectRoot, 'functions/_shared/cardManifest.generated.js');
const frontendNamesPath = resolve(projectRoot, 'src/data/cardNames.generated.js');

const index = JSON.parse(readFileSync(indexPath, 'utf8'));
const cardIds = [];
const factions = new Set();
const cardsByIdEntries = [];

for (const shard of index.shards) {
  const shardPath = resolve(projectRoot, 'public', shard.path.replace(/^\//, ''));
  const cards = JSON.parse(readFileSync(shardPath, 'utf8'));
  if (!Array.isArray(cards)) {
    console.error(`FAIL: shard ${shard.path} is not an array`);
    process.exit(1);
  }
  if (cards.length !== shard.count) {
    console.error(
      `FAIL: shard ${shard.path} count mismatch (index=${shard.count}, actual=${cards.length})`,
    );
    process.exit(1);
  }
  for (const card of cards) {
    if (!card?.id || typeof card.id !== 'string') {
      console.error(`FAIL: shard ${shard.path} has card without string id`);
      process.exit(1);
    }
    cardIds.push(card.id);
    if (card.faction) factions.add(card.faction);
    cardsByIdEntries.push([
      card.id,
      {
        faction: card.faction ?? '',
        type: card.type ?? '',
        name: card.name ?? card.id,
      },
    ]);
  }
}

if (cardIds.length !== index.total) {
  console.error(`FAIL: total mismatch (index=${index.total}, actual=${cardIds.length})`);
  process.exit(1);
}

const uniqueIds = new Set(cardIds);
if (uniqueIds.size !== cardIds.length) {
  console.error('FAIL: duplicate card ids across shards');
  process.exit(1);
}

mkdirSync(dirname(outputPath), { recursive: true });

const sortedIds = [...uniqueIds].sort();
const sortedFactions = [...factions].sort();
const sortedCardsById = cardsByIdEntries
  .filter(([id]) => uniqueIds.has(id))
  .sort(([a], [b]) => a.localeCompare(b));

const content = `// Generated by scripts/generate-card-manifest.mjs — do not edit.
export const cardIds = new Set(${JSON.stringify(sortedIds)});
export const factions = new Set(${JSON.stringify(sortedFactions)});
export const cardsById = new Map(${JSON.stringify(sortedCardsById)});
`;

writeFileSync(outputPath, content, 'utf8');

const cardNames = Object.fromEntries(
  sortedCardsById.map(([id, meta]) => [id, meta.name]),
);
mkdirSync(dirname(frontendNamesPath), { recursive: true });
writeFileSync(
  frontendNamesPath,
  `// Generated by scripts/generate-card-manifest.mjs — do not edit.\nexport const cardNamesById = ${JSON.stringify(cardNames, null, 2)};\n`,
  'utf8',
);

console.log(
  `OK: card manifest generated (${sortedIds.length} cards, ${sortedFactions.length} factions)`,
);
